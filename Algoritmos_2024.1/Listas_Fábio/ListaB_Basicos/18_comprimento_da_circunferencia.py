#Leia o valor do raio de uma circunferência, calcule e escreva seu 
#comprimento.(c = 2 * pi * r)

#Entrada
raio = float(input('\nDigite o valor do raio da circunferência: cm '))

#Processamento
PI = 3.14
comprimento = 2 * PI * raio

#Saída
print(f'\nComprimento total: {comprimento:.2f} cm')
