#Leia o valor da base e altura de um retângulo, calcule e escreva sua área.
#(área = base * altura)

#Entrada
base = float(input('\nDigite o valor da base do retângulo: cm '))
altura = float(input('Agora digite o valor da altura: cm '))

#Processamento
area = base * altura

#Saída
print(f'\nÁrea: {area:.2f} cm')
